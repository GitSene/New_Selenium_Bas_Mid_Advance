package BasicJava;

public class DataTypeAndVariables {

	public static void main(String[] args) {
		// This program is to learn Data type and variables
		String name = "Babek";
		double hieght = 5.7;
		double weight = 149;
		int age = 34;
		char gender = 'M';  // Char value Must be in single quotation not double!!!
		
		System.out.println("My name is:" + name);
        System.out.println("My hieght is:"+ hieght +  "  feet");
        System.out.println("My weight is:"+ weight +  "  pounds");
        System.out.println("My age is:"+ age);
        System.out.println("My gender is:"+ gender);
	}

}
