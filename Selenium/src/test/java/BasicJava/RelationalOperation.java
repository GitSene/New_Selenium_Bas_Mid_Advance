package BasicJava;

public class RelationalOperation {

	public static void main(String args[] ) {
		// Relational Operation
		// ==, !=,<, >, =<, => 
		int a = 5;
		int b = 8;
		System.out.println("is "+ a + "==" + b +"?  " + (a==b) );
		System.out.println("is "+ a + "!=" + b +"?  " + (a!=b) );
		System.out.println("is "+ a + ">" + b +"?  " + (a>b) );
		System.out.println("is "+ a + ">=" + b +"?  " + (a>=b) );
		System.out.println("is "+ a + "<" + b +"?  " + (a<b) );
		System.out.println(a<=b); // It just indicate true or false

	}

}
